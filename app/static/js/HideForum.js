function HideForum()
{
    const MAIN_BLUR = document.getElementById('main-blur');
    const MAIN_BLUR_STYLES = MAIN_BLUR.style.filter

    if (MAIN_BLUR_STYLES.filter = 'blur(10px)')
        MAIN_BLUR.style.filter = 'blur(10px)';
    else
        MAIN_BLUR.style.filter = 'initial';
}

